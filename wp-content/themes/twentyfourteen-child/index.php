<h2>People with Cystic Fibrosis can't meet; We can't have WordCamps</h2>

<p>
Studies have shown that it’s dangerous for people with Cystic Fibrosis to hang out in real life. <strong>We can’t have WordCamps</strong>. We only have the internet to connect. Kids growing up with Cystic Fibrosis these days never even come into contact with each other. Because of a high susceptibility to lung infections, we can easily infect each other just by hanging out.</p> 
<blockquote>
Highly resistant bacteria can be shared between those with CF that can lead to serious declines in lung function and more vicious illnesses that are harder to treat, ultimately reducing life expectancy
</blockquote>
<p>
I want to create a place online for us, the Cystic Fibrosis community. Not only for my friends also for young kids with CF that are growing up now. For their parents who are often just as isolated from meeting others in the same position as them.
</p>

<p>
<blockquote>We need an international community like this. Not Facebook. No commercial software. A place by people with CF, for people with CF. 100% build on Open Source software.</blockquote>
</p>

<h2>CFCommunity.net: An community for people with Cystic Fibrosis</h2>
<img src="http://cfcommunity.net/wp-content/uploads/2014/01/updates-1024x731.jpg" alt="image">
