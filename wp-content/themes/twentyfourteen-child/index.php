<br>
<blockquote>
The idea is simple: A social network for people with CF, by people with CF. Powered by WordPress/BuddyPress and with a Multisite setup so people can create their own sites. We can be part of a group of people that know what it’s like to have CF or be around someone who has it, without running the risk of cross-infection.
</blockquote>

It will not only be a place where you meet others with CF, it can also serve as the way for a younger generation to learn more about their disease through meeting kids who are in the same position. As someone who’ve had the luxury to meet others with CF in real life, I know how important this is.
</p>

